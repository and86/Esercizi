package rubrica;

public class Voce {
	
	private String nome;
	private String cogname;
	private String tel;
	
	public Voce() {
		
	}

	public Voce(String nome, String cogname, String tel) {
		this.nome = nome;
		this.cogname = cogname;
		this.tel = tel;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCogname() {
		return cogname;
	}

	public void setCogname(String cogname) {
		this.cogname = cogname;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}
	
	
	
	
	
	

}
